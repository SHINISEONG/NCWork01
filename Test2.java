public class Test2{
	public static void main(String[] args){
		
		if (args.length==2)
		{
			int a = Integer.parseInt(args[0]);
			int b = Integer.parseInt(args[1]);
			String msg = "";
			if (a!=b)
			{
				msg = (a>b)? ("첫 번째 인수 : " + a + ", 두 번째 인수 : " + b +", "+ a + "-" + b + "=" + (a-b)+"입니다.") : ("첫 번째 인수 : " + a + ", 두 번째 인수 : " + b +", "+ b + "+" + a + "=" + (a+b)+"입니다.");
			}
			else {
				msg = "두 수가 같네요";
			}
				System.out.println(msg);

		} else {
			System.out.println("인수는 2가지로 입력해 주세요");
		}
	}//end of main
}//end of class